package learn;
/*
 * 算术运算符
 */
public class OperatorDemo {
	public static void main (String[]args) {
		int a = 6;
		int b = 4;
		System.out.println(a + b);
		System.out.println(a - b);
		System.out.println(a * b);
		System.out.println(a / b);
		System.out.println(a % b);
		// 得到小数
		System.out.println(6.0 / 4);
		
	}

}
