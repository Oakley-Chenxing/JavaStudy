package learn;
/*
 * 类型转换
 */
public class ConversionDemo {
	public static void main(String[] args){
		//自动类型转换
		double a = 10;
		System.out.println(a);
		byte b = 10;
		System.out.println(b);
		short s = b;
		System.out.println(s);
		int i =b ;
		System.out.println(i);
		//强制转换
		int k =(int)88.88;
		System.out.println(k);
	}
        

}
